nota01 = float(input('Qual foi a primeira nota?'))
nota02 = float(input('Qual foi a segunda nota?'))
nota03 = float(input('Qual foi a terceira nota?'))
media = (nota01+nota02+nota03)/3
print('A primeira nota foi {}, a segunda foi {}, a terceira nota foi {} é a média foi {:.2f}.'.format(nota01, nota02, nota03, media))